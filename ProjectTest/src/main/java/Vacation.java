public class Vacation {
    private String dateVacation;
    private String timeVacation;

    public String getDateVacation() {
        return dateVacation;
    }

    public void setDateVacation(String dateVacation) {
        this.dateVacation = dateVacation;
    }

    public String getTimeVacation() {
        return timeVacation;
    }

    public void setTimeVacation(String timeVacation) {
        this.timeVacation = timeVacation;
    }
}
